import { convertToWords } from "../src/convertToWords";



describe('Convert number to words', () => {
   
    it('should convert number to words', () => {
        expect(convertToWords(381401152)).toBe('триста осумдесет и еден милион четиристотини и една илјада сто педесет и два');
        expect(convertToWords(250725235)).toBe('двесте и педесет милиони седумстотини дваесет и пет илјади двесте триесет и пет');
        expect(convertToWords(1000000000)).toBe('милијарда');
        expect(convertToWords(123456789)).toBe('сто дваесет и три милиони четиристотини педесет и шест илјади седумстотини осумдесет и девет');
        expect(convertToWords(987654321)).toBe('деветстотини осумдесет и седум милиони шестотини педесет и четири илјади триста дваесет и еден');
        expect(convertToWords(555555555)).toBe('петстотини педесет и пет милиони петстотини педесет и пет илјади петстотини педесет и пет');
        expect(convertToWords(101010101)).toBe('сто и еден милион десет илјади сто и еден');
        expect(convertToWords(777777777)).toBe('седумстотини седумдесет и седум милиони седумстотини седумдесет и седум илјади седумстотини седумдесет и седум');
        expect(convertToWords(369258147)).toBe('триста шеесет и девет милиони двесте педесет и осум илјади сто четириесет и седум');
        expect(convertToWords(147258369)).toBe('сто четириесет и седум милиони двесте педесет и осум илјади триста шеесет и девет');
        expect(convertToWords(999999999)).toBe('деветстотини деведесет и девет милиони деветстотини деведесет и девет илјади деветстотини деведесет и девет');
        expect(convertToWords(100000001)).toBe('сто милиони и еден');
        expect(convertToWords(100001001)).toBe('сто милиони една илјада и еден');
        expect(convertToWords(101001001)).toBe('сто и еден милион една илјада и еден');
        expect(convertToWords(500000050)).toBe('петстотини милиони и педесет');
        // New 8-digit number tests
        expect(convertToWords(12345678)).toBe('дванаесет милиони триста четириесет и пет илјади шестотини седумдесет и осум');
        expect(convertToWords(87654321)).toBe('осумдесет и седум милиони шестотини педесет и четири илјади триста дваесет и еден');
        expect(convertToWords(10000000)).toBe('десет милиони');
        expect(convertToWords(99999999)).toBe('деведесет и девет милиони деветстотини деведесет и девет илјади деветстотини деведесет и девет');
        expect(convertToWords(50505050)).toBe('педесет милиони петстотини и пет илјади и педесет');
        expect(convertToWords(11111111)).toBe('единаесет милиони сто единаесет илјади сто и единаесет');
        expect(convertToWords(33333333)).toBe('триесет и три милиони триста триесет и три илјади триста триесет и три');
        expect(convertToWords(77777777)).toBe('седумдесет и седум милиони седумстотини седумдесет и седум илјади седумстотини седумдесет и седум');
        expect(convertToWords(20000001)).toBe('дваесет милиони и еден');
        expect(convertToWords(40004000)).toBe('четириесет милиони и четири илјади');
        // New 7-digit number tests
        expect(convertToWords(1234567)).toBe('милион двесте триесет и четири илјади петстотини шеесет и седум');
        expect(convertToWords(7654321)).toBe('седум милиони шестотини педесет и четири илјади триста дваесет и еден');
        expect(convertToWords(1000000)).toBe('милион');
        expect(convertToWords(9999999)).toBe('девет милиони деветстотини деведесет и девет илјади деветстотини деведесет и девет');
        expect(convertToWords(5050505)).toBe('пет милиони педесет илјади петстотини и пет');
        expect(convertToWords(1111111)).toBe('милион сто единаесет илјади сто и единаесет');
        expect(convertToWords(3333333)).toBe('три милиони триста триесет и три илјади триста триесет и три');
    });
    
});