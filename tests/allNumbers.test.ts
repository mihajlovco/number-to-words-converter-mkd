import { toWords } from "../src/index";



describe('Convert number to words', () => {
   
    it('should correctly convert 9-digit numbers to words - billions', () => {
        expect(toWords(381401152)).toBe('триста осумдесет и еден милион четиристотини и една илјада сто педесет и два');
        expect(toWords(250725235)).toBe('двесте и педесет милиони седумстотини дваесет и пет илјади двесте триесет и пет');
        expect(toWords(1000000000)).toBe('милијарда');
        expect(toWords(123456789)).toBe('сто дваесет и три милиони четиристотини педесет и шест илјади седумстотини осумдесет и девет');
        expect(toWords(987654321)).toBe('деветстотини осумдесет и седум милиони шестотини педесет и четири илјади триста дваесет и еден');
        expect(toWords(555555555)).toBe('петстотини педесет и пет милиони петстотини педесет и пет илјади петстотини педесет и пет');
        expect(toWords(101010101)).toBe('сто и еден милион десет илјади сто и еден');
        expect(toWords(777777777)).toBe('седумстотини седумдесет и седум милиони седумстотини седумдесет и седум илјади седумстотини седумдесет и седум');
        expect(toWords(369258147)).toBe('триста шеесет и девет милиони двесте педесет и осум илјади сто четириесет и седум');
        expect(toWords(147258369)).toBe('сто четириесет и седум милиони двесте педесет и осум илјади триста шеесет и девет');
        expect(toWords(999999999)).toBe('деветстотини деведесет и девет милиони деветстотини деведесет и девет илјади деветстотини деведесет и девет');
        expect(toWords(100000001)).toBe('сто милиони и еден');
        expect(toWords(100001001)).toBe('сто милиони една илјада и еден');
        expect(toWords(101001001)).toBe('сто и еден милион една илјада и еден');
        expect(toWords(500000050)).toBe('петстотини милиони и педесет');
             
    });

    it('should correctly convert 8-digit numbers to words - millions', () => {
        expect(toWords(12345678)).toBe('дванаесет милиони триста четириесет и пет илјади шестотини седумдесет и осум');
        expect(toWords(87654321)).toBe('осумдесет и седум милиони шестотини педесет и четири илјади триста дваесет и еден');
        expect(toWords(10000000)).toBe('десет милиони');
        expect(toWords(99999999)).toBe('деведесет и девет милиони деветстотини деведесет и девет илјади деветстотини деведесет и девет');
        expect(toWords(50505050)).toBe('педесет милиони петстотини и пет илјади и педесет');
        expect(toWords(11111111)).toBe('единаесет милиони сто единаесет илјади сто и единаесет');
        expect(toWords(33333333)).toBe('триесет и три милиони триста триесет и три илјади триста триесет и три');
        expect(toWords(77777777)).toBe('седумдесет и седум милиони седумстотини седумдесет и седум илјади седумстотини седумдесет и седум');
        expect(toWords(20000001)).toBe('дваесет милиони и еден');
        expect(toWords(40004000)).toBe('четириесет милиони и четири илјади');  
    });
    
    it('should correctly convert 7-digit numbers to words', () => {
        expect(toWords(1234567)).toBe('милион двесте триесет и четири илјади петстотини шеесет и седум');
        expect(toWords(7654321)).toBe('седум милиони шестотини педесет и четири илјади триста дваесет и еден');
        expect(toWords(1000000)).toBe('милион');
        expect(toWords(9999999)).toBe('девет милиони деветстотини деведесет и девет илјади деветстотини деведесет и девет');
        expect(toWords(5050505)).toBe('пет милиони педесет илјади петстотини и пет');
        expect(toWords(1111111)).toBe('милион сто единаесет илјади сто и единаесет');
        expect(toWords(3333333)).toBe('три милиони триста триесет и три илјади триста триесет и три');
    });

    it('should correctly convert 6-digit numbers to words', () => {
        expect(toWords(123456)).toBe('сто дваесет и три илјади четиристотини педесет и шест');
        expect(toWords(789012)).toBe('седумстотини осумдесет и девет илјади и дванаесет');
        expect(toWords(456789)).toBe('четиристотини педесет и шест илјади седумстотини осумдесет и девет');
        expect(toWords(234567)).toBe('двесте триесет и четири илјади петстотини шеесет и седум');
        expect(toWords(901234)).toBe('деветстотини и една илјада двесте триесет и четири');
        expect(toWords(345678)).toBe('триста четириесет и пет илјади шестотини седумдесет и осум');
        expect(toWords(567890)).toBe('петстотини шеесет и седум илјади осумстотини и деведесет');
        expect(toWords(112233)).toBe('сто дванаесет илјади двесте триесет и три'); // треба граматичка проверка
        expect(toWords(445566)).toBe('четиристотини четириесет и пет илјади петстотини шеесет и шест');
        expect(toWords(778899)).toBe('седумстотини седумдесет и осум илјади осумстотини деведесет и девет');
        expect(toWords(100001)).toBe('сто илјади и еден');
        expect(toWords(100010)).toBe('сто илјади и десет');
        expect(toWords(100200)).toBe('сто илјади и двесте');
        expect(toWords(101000)).toBe('сто и една илјада');
        expect(toWords(110000)).toBe('сто и десет илјади');
      });

      it('should correctly convert 5-digit numbers to words', () => {
        expect(toWords(12345)).toBe('дванаесет илјади триста четириесет и пет');
        expect(toWords(98765)).toBe('деведесет и осум илјади седумстотини шеесет и пет');
        expect(toWords(54321)).toBe('педесет и четири илјади триста дваесет и еден');
        expect(toWords(20202)).toBe('дваесет илјади двесте и два');
        expect(toWords(33333)).toBe('триесет и три илјади триста триесет и три');
        expect(toWords(45678)).toBe('четириесет и пет илјади шестотини седумдесет и осум');
        expect(toWords(87654)).toBe('осумдесет и седум илјади шестотини педесет и четири');
        expect(toWords(60000)).toBe('шеесет илјади');
        expect(toWords(11111)).toBe('единаесет илјади сто и единаесет');
        expect(toWords(10001)).toBe('десет илјади и еден');
        expect(toWords(10010)).toBe('десет илјади и десет');
        expect(toWords(10200)).toBe('десет илјади и двесте');
        expect(toWords(10230)).toBe('десет илјади двесте и триесет');
      });

      test('converts 4-digit numbers correctly', () => {
        expect(toWords(1000)).toBe('илјада');
        expect(toWords(1001)).toBe('илјада и еден');
        expect(toWords(1010)).toBe('илјада и десет');
        expect(toWords(1100)).toBe('илјада и сто');
        expect(toWords(1101)).toBe('илјада сто и еден');
        expect(toWords(2345)).toBe('две илјади триста четириесет и пет');
        expect(toWords(5678)).toBe('пет илјади шестотини седумдесет и осум');
        expect(toWords(9000)).toBe('девет илјади');
        expect(toWords(9999)).toBe('девет илјади деветстотини деведесет и девет');
        expect(toWords(4221)).toBe('четири илјади двесте дваесет и еден');
      });

      test('converts zero to word', () => {
        expect(toWords(0)).toBe('нула');
      });
});